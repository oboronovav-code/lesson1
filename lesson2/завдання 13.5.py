#5. Реалізувати алгоритм обчислення НСД (найбільшого спільного дільника) і
#НСК (найменшого спільного кратного) для двох чисел.
a = int(input("Введіть перше число: "))
b = int(input("Введіть друге число: "))
c = a
d = b
while b:
    a, b = b, a % b
print("Найбільший спільний дільник (НСД) = ",a)

print("Найменше спільне кратне (НСК) = ",abs(c * d) // a)
