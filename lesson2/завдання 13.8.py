#8. Написати програму, яка зсуває всі елементи масиву на (k) позицій праворуч
#(значення (k) вводить користувач).
import random
lst = []
for i in range(10):
    lst.append(random.randint(-15, 15))
print("Наявний список: ", lst)
k = int(input("Введіть кількість позицій, на яку ви хочете здвинути елементи списку:"))
for i in range(k):
    lst.insert(0, "")
print("Змінений список:",lst)
