#4. Написати програму, яка друкує таблицю чисел від 1 до (n), але для кожного
#числа вказує також його квадрат, куб та корінь (формат: число, квадрат, куб,
#корінь).
import math
n = int(input("Введіть останнє число вашої таблиці:"))
print("Формат: число, квадрат, куб, корінь")
for i in range(1, n+1):
    print(i,",", i**2,",", i**3,",", math.sqrt(i))
